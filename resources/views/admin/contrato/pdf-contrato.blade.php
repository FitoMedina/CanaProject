    <p>
    Entre el Sr. Ing. Germán Caro Benitez con documento de identidad Nro. 3214012 sc, con domicilio en la calle La Paz 420 en la ciudad de Montero, en adelante <strong>“EL EMPLEADOR”</strong> y el Sr. Con cédula de identidad Nro………………………. Con domicilio …………………………. Portador del brevet categoría ………………… Nro…………….. , en adelante <strong>“EL TRABAJADOR”</strong>, convienen a celebrar el presente contrato <strong>DE TRABAJO A PLAZO FIJO</strong>, sujeta a las clausulas y condiciones siguientes:
    </p>
    <p>
    <strong>PRIMERA.- “EL TRABAJADOR”</strong> se compromete y obliga  desempeñar el servicio laboral de CHOFER O CONDUCTOR de un camión o vehículo de transporte ce caña de azúcar que el empleador le asigne para transportar la caña de azúcar hasta la industria azucarera (Ingenio Azucarero Guabirá) y en toda otra labor que se le asigne de acuerdo a su capacidad y conocimiento dentro de las actividades de <strong>“EL EMPLEADOR”</strong>.
    </p>
    <p>
    <strong>SEGUNDA.-</strong> El presente contrato es de plazo definido desde el inicio de zafra hasta su conclusión del presente año 2018.
    </p>
    <p>
    <strong>TERCERA.- “EL TRBAJADOR”</strong> prestará sus servicios personales conforme a lo previsto en el último parágrafo del art. 46 de la L.G.T, adecuándose a los horarios discontinuos de entrega de la caña de azúcar a la industria azucarera durante la vigencia de la zafra cañera.
    </p>
    <p>
    <strong>CUARTA.-</strong> Obligaciones de las partes:
    </p>
    <p>
    <strong>De “EL TRABAJADOR”</strong>
    </p>
    <p>
        <ol type="a"> 
            <li>Cumplir con su actividad laboral diligentemente, conduciendo el motorizado de acuerdo a las regulaciones de la normativa de Tránsito ( Cod. Y reglamento de Tránsito). 
            <li>El manejo del motorizado la efectuará tomando las medidas de seguridad y cuidados necesarios, cumpliendo las instructivas para evitar accidentes y daños personales.
            <li>Mantener asistencia y puntualidad en el trabajo a desempeñar. 
            <li>Efectuar el mantenimiento periódico, buen uso y conservación del motorizado, así como  de las herramientas a su cargo, debiendo en forma oportuna informar a <strong>“EL EMPLEADOR”</strong> en el caso de presentar fallas mecánicas.
            <li>Estando prohibido de consumir bebidas alcohólicas y estupefacientes durante el desempeño de su actividad laboral, como también no podrá concurrir a su fuente de trabajo en estado de ebriedad.
            <li>Observará una adecuada conducta moral en el desarrollo de sus actividades y/o preste su fuerza laboral, estando prohibido de incurrir en violencia, escándalos, reyertas, hurtos, robos, etc.
            <li>Cumplirá con los programas de las Buenas Prácticas Laborales y Bonsurco.
            <li>En el caso de retiro de la actividad laboral antes del vencimiento del presente contrato, hará conocer a <strong>“EL EMPLEADOR”</strong> su retiro con una anticipación de 30 días de anticipación, a objeto de que este último no se vea perjudicado en sus actividades productivas.
        </ol>
    </p>
    <p>
    <strong>De “EL EMPLEADOR”</strong>
    </p>
    <ol type="a"> 
        <li>Pagar la remuneración comprometida en forma mensual
        <li>Cancelar la indemnización a la finalización de la relación laboral
        <li>Proporcionar los medios para el cumplimiento del presente contrato
    </ol>
    <p>
    <strong>QUINTA.-</strong> Será causal de rescisión del presente contrato, las causales previstas en el art. 16 de la L.G.T  y art. 9 de su Decreto Reglamentario.
    </p>
    <p>
    <strong>SEXTA.- “EL EMPLEADOR”</strong> pagará a <strong>“EL TRABAJADOR”</strong>, por la prestación  del servicio, un salario básico mensual hasta la finalización del presente contrato, de <strong>Bs. 2060.-</strong> ( Dos mil sesenta bolivianos 00/100), más adicionalmente recibirá los recargos que le pudieran  corresponder de acuerdo a ley. 
    </p>
    <p>
    <strong>SEPTIMA.-</strong> Conforme a lo previsto en el art. 7 del D.R de la L.G.T., <strong>“EL TRABAJADOR”</strong> señala como sus herederos: ……………………………………………
    </p>
    <p>
    <strong>OCTAVA.-</strong> La actividad laboral establecida en el presente contrato se desenvolverá en el respeto mutuo entre la parte empleadora y laboral, sin actitudes o motivos racistas y/o discriminatorias de ninguna índole, conforme a lo dispuesto en la Ley No 45 de fecha 08 de octubre de 2010 y su decreto reglamentario.
    </p>
    <p>
    <strong>NOVENA.-</strong> Ambas partes aceptan el tenor del presente contrato y se someten  a su estricto cumplimiento, firmando al pie en tres ejemplares de un mismo tenor y para un mismo efecto.
